# List
fruits = ["Apples", "Banana", "Mango", "Grapes"]

print(fruits)

# List Methods
fruits.append("WaterMelon")
print(fruits)

fruits.pop()
print(fruits)

fruits.insert(3, "WaterMelon")
print(fruits)

fruits.insert(-4, "Orange")
print(fruits)

fruits.remove("Mango")
print(fruits)

fruits.sort()
print(fruits)

letters = ["D", "F", "C", "A", "E", "B"]
print(letters)

letters.sort()
print(letters)

for fruit in fruits:
    print(fruit)  
